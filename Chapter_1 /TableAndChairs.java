public class TableAndChairs {
    public static void main(String[] args) {

        for (int i = 1; i <= 3; i++) {
            System.out.println("|                               |");
        }


        System.out.println("|_ _ _ _     _ _ _ _     _ _ _ _|");

        for (int i = 1; i <= 3; i++) {
            System.out.println("|      |     |     |     |      |");
        }
    }
}
